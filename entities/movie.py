class Movie:

    def __init__(self, moviesId, movie_name, release_date, rating, genre, studioId, boxofficeId, directorsId, actorsId, genresId):
        self.moviesId = moviesId
        self.movie_name = movie_name
        self.release_date = release_date
        self.rating = rating
        self.genre = genre
        self.studioId = studioId
        self.boxofficeId = boxofficeId
        self.directorsId = directorsId
        self.actorsId = actorsId
        self.genresId = genresId
